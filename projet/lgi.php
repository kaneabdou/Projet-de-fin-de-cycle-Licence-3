			<p class="top para">Le Département Informatique creer en 2007, forme des professionnels à caractère généraliste et multi-métiers.
			 Il s'agit là d'un vrai atout du département et d'une marque de différentiation par rapport à nos « concurrents ».
			 Le Département Informatique a toujours su résister aux phénomènes de modes tellement fréquents en informatique.
				L’Unité de Formation et de Recherche (UFR) Sciences et Technologies(SET) de l’université Thiés accueille chaque année de nouveaux étudiants pour les former en Sciences et se donne le défi de faire d’eux les meilleurs de la sous-région.
Grâce à l’engouement et au dévouement professionnel de son corps professoral et administratif, l’UFR SET envisage de couvrir les besoins d'apprentissage et de fournir un accès complet à la connaissance de ses apprenants.
				La jeunesse, le dynamisme et la très haute qualité de ses acteurs, issues d’une pluralité de disciplines contribuera sans nul doute à la mise en œuvre des projets de l’UFR. C’est par la recherche et l’innovation que son Personnel d’Enseignement et de Recherche, à travers des thématiques centrées autour des préoccupations des populations locales, parviendra à trouver les solutions aux problèmes qui se posent à nos institutions en général et à notre société en particulier. En effet, l’UFR a participé au service à la communauté en permettant au département de TIC de former des agents de l’administration de Bambey (préfecture, mairie).
Avec les contrats de performance l’UFR SET a bien évolué. Nous ne citerons que quelques exemples à cet égard : la formation des enseignants à la formation à distance et au système LMD, la finalisation des syllabi, l’acquisition de matériel de Travaux Pratiques en physique, en chimie et en informatique, la confection du guide LMD, de fiches diplôme, du livret de l’étudiant, de l’agenda de l’étudiant entre autres, la réalisation des sessions de mise à niveau, de techniques d’apprentissage, de remédiation et de préparations aux examens.
L’utilisation d’un logiciel de gestion des notes dénommé LMD pro pour automatiser les inscriptions, les délibérations, les relevés et les attestations est maintenant effective.
